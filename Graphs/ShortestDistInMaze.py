"""
Given a matrix of integers A of size N x M describing a maze. The maze consists of empty locations and walls.
1 represents a wall in a matrix and 0 represents an empty location in a wall.
There is a ball trapped in a maze. The ball can go through empty spaces by rolling up, down, left or right, but it won't stop rolling until hitting a wall. When the ball stops, it could choose the next direction.
Given two array of integers of size B and C of size 2 denoting the starting and destination position of the ball.
Find the shortest distance for the ball to stop at the destination. The distance is defined by the number of empty spaces traveled by the ball from the starting position (excluded) to the destination (included). If the ball cannot stop at the destination, return -1.
"""
from collections import defaultdict
from heapq import heappop, heappush
class Solution:
    def formedge(self, i, j,  A):
        if i >= self.n or j >= self.m: return []
        edges = []
        def nodename(i , j):
            return 100 * i + j
        ti, tj = i, j
        while ti - 1 >= 0 and A[ti - 1][tj] == 0:
            ti-=1
        if ti != i: edges.append((i-ti, nodename(ti, tj)))
        ti, tj = i, j
        while tj - 1 >= 0 and A[ti][tj - 1] == 0:
            tj-=1
        if j != tj: edges.append((j-tj, nodename(ti, tj)))
        ti, tj = i, j
        while ti + 1 < self.n and A[ti + 1][tj] == 0:
            ti+=1
        if i != ti: edges.append((ti-i, nodename(ti, tj)))
        ti, tj = i, j
        while tj + 1 < self.m and A[ti][tj + 1] == 0:
            tj+=1
        if tj != j: edges.append((tj-j, nodename(ti, tj)))
        return edges






    def solve(self, A, B, C):
        edges = defaultdict(list)
        self.n, self.m = len(A), len(A[0])
        #print(self.n)
        s, e = 10 * B[0] + B[1], 10 * C[0] + C[1]
        h = [(0, s)]
        visited = set()
        while h:
            d, n = heappop(h)
            if n == e: return d
            visited.add(n)
            for c in self.formedge(n//10, n%10, A):
                if c[1] not in visited:
                    heappush(h, (d + c[0], c[1]))
        return -1



